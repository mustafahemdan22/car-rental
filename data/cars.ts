/** Helper to build a car's full image set: 1 cover + N gallery images */
function buildCarImages(slug: string, color: string, galleryCount: number = 3): {
  coverImage: CarImage;
  gallery: CarImage[];
} {
  return {
    coverImage: createMockCarImage(slug, 'cover', 0, color),
    gallery: Array.from({ length: galleryCount }, (_, i) =>
      createMockCarImage(slug, 'gallery', i, color)
    ),
  };
}

